// ── Context ──────────────────────────────────────────────────────────
const Ctx = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: TKey) => string;
}>({ lang: "en", setLang: () => {}, t: (k) => k });

export function LangProvider({
  children,
  initialLang,
}: {
  children: React.ReactNode;
  initialLang?: string; // işletmenin varsayılan dili (kayıt/tarayıcı dili yoksa)
}) {
  // SSR/ilk render: işletme varsayılanı (varsa), yoksa 'en'
  const [lang, setLangState] = useState<Lang>(normalizeLang(initialLang) ?? "en");

  // Öncelik: 1) kayıtlı kullanıcı tercihi, 2) tarayıcı/telefon dili,
  // 3) işletme varsayılanı, 4) 'en'
  useEffect(() => {
    let next: Lang | null = null;
    try {
      next = normalizeLang(localStorage.getItem(STORAGE_KEY));
    } catch {}
    if (!next) next = normalizeLang(navigator.language);
    if (!next) next = normalizeLang(initialLang);
    setLangState(next ?? "en");
  }, [initialLang]);

  // <html lang="..."> güncel kalsın (erişilebilirlik/SEO)
  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const setLang = (l: Lang) => {
    setLangState(l);
    try {
      localStorage.setItem(STORAGE_KEY, l);
    } catch {}
  };

  // Aktif dil -> İngilizce -> anahtarın kendisi
  const t = (k: TKey) => I18N[lang]?.[k] ?? I18N.en[k] ?? k;

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useLang() {
  return useContext(Ctx);
}

// Çok dilli menü içeriğinden aktif dili seç; yoksa fallback (de→tr→en) ya da eski düz metin.
// (Dinamik içerik yalnızca tr/de/en tutar; diğer arayüz dillerinde bu zincire düşer.)
export function pickLang(
  i18n: I18nText | null | undefined,
  fallback: string,
  lang: Lang
): string {
  return (i18n && (i18n[lang] || i18n.de || i18n.tr || i18n.en)) || fallback || "";
}

// Sipariş durumunu yerelleştir
export function statusLabel(status: string, t: (k: TKey) => string): string {
  if (status === "yeni") return t("stNew");
  if (status === "hazirlaniyor") return t("stPreparing");
  if (status === "tamamlandi") return t("stDone");
  return status;
}
