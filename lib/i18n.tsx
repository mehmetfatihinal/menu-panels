export const SUPPORTED_LANGS = [
  { code: "de", label: "Deutsch" },
  { code: "tr", label: "Türkçe" },
  { code: "en", label: "English" },
  { code: "fr", label: "Français" },
  { code: "ar", label: "العربية" },
  { code: "ps", label: "پښتو" },
  { code: "prs", label: "دری" },
] as const;

export type Lang = (typeof SUPPORTED_LANGS)[number]["code"];

const STORAGE_KEY = "mp-lang";

// "de-DE" -> "de" gibi sadeleştirir; desteklenmeyen dilde null döner.
// Not: Afgan telefonları Darice için çoğunlukla "fa-AF"/"fa" bildirir -> "prs".
export function normalizeLang(raw?: string | null): Lang | null {
  if (!raw) return null;
  const v = raw.trim().toLowerCase();
  if (v === "fa" || v.startsWith("fa-") || v === "prs" || v.startsWith("prs-"))
    return "prs";
  const base = v.split("-")[0];
  return SUPPORTED_LANGS.some((l) => l.code === base) ? (base as Lang) : null;
}

